
public class OperadoresAritmeticos {

	public static void main(String[] args) {
		// Literales
		int i1 = 1;
		int i2 = 2;
		long l1 = 100;
		long l2 = i1 + l1;
		int i3 = 5 / 2; // -> 2
		double d1 = 5 / 2; // -> 2.0
		double d2 = 5.0 / 2; // -> 2.5
		
		int i4 = 5 % 2; // -> 1
		
		int i5 = 1;
		System.out.println(i5);
		System.out.println(i5++);
		System.out.println(i5);
		System.out.println(++i5);
		System.out.println(i5);
		
	}
}
